public class BelajarIfElse{
    public static void main(String[] args) {
        int nilai = 74;

        if(nilai > 75){
            System.out.println("Saya Lulus");
        }
        else{
            System.out.println("Saya Tidak Lulus Dan Harus Mengulang");
        }
        System.out.println("Saya Akan Masuk Kuliah");
    }
}